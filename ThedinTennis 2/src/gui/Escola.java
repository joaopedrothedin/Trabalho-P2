    private void btngravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngravarActionPerformed

        model.Empresa empresa = new model.Empresa();
        empresa.setEmpresa(txtnome.getText());
        empresa.setEndereco(txtendereco.getText());

        try {
            ControleEmpresa.cadastrar(empresa);
        } catch (SQLException ex) {
            Logger.getLogger(model.Empresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            listaempresa.setListData(ControleEmpresa.atualizarLista().toArray());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        //limpar tela;
        txtnome.setText("");
        txtendereco.setText("");

        // TODO add your handling code here:
    }//GEN-LAST:event_btngravarActionPerformed

    private void btnalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalterarActionPerformed
        model.Empresa empresa = new model.Empresa();
        empresa.setEmpresa(txtnome.getText());
        empresa.setEndereco(txtendereco.getText());
        empresa.setId(Integer.parseInt(id.getText()));

        try {
            ControleEmpresa.alterar(empresa);
        } catch (SQLException ex) {
            Logger.getLogger(model.Empresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            listaempresa.setListData(ControleEmpresa.atualizarLista().toArray());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        //limpar tela;
        txtnome.setText("");
        txtendereco.setText("");
    }//GEN-LAST:event_btnalterarActionPerformed

