    private void listaempresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaempresaMouseClicked
        model.Empresa e = (model.Empresa) listaempresa.getSelectedValue();
        txtnome.setText(e.getEmpresa());
        txtendereco.setText(e.getEndereco());
        id.setText(e.getId().toString());

    
    }//GEN-LAST:event_listaempresaMouseClicked

    private void btnremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoverActionPerformed
        if (listaempresa.getSelectedValue() != null) {
            model.Empresa empresa = new model.Empresa();
            empresa.setEmpresa(txtnome.getText());
            empresa.setEndereco(txtendereco.getText());
            empresa.setId(Integer.parseInt(id.getText()));
            try {
                ControleEmpresa.excluir(empresa.getId());
            } catch (SQLException ex) {
                Logger.getLogger(model.Empresa.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                listaempresa.setListData(ControleEmpresa.atualizarLista().toArray());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            // TODO add your handling code here:
    }//GEN-LAST:event_btnremoverActionPerformed
    }

